    private void BotonAgregarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarVActionPerformed
        // TODO add your handling code here:
        
        //Acá toma todos los datos y los reescribe en la matriz
        
        String CodVehiculo=TxtCodVehiculo.getText();
        String Marca=txtMarca.getText();
        String Patente=txtPatente.getText();
        String Estado=jComboBox1.getSelectedItem().toString();
        String DatosV[]={CodVehiculo,Marca,Patente,Estado};
        modelV.addRow(DatosV);
        
        //Acá graba la información
        //Guarda datos en archivo .txt
        
        try{
          
                FileWriter salvar=new FileWriter ("DatosVehiculos.txt");
                for(int j=0;j<this.TablaVehiculos.getRowCount();j++){
                    salvar.write(modelV.getValueAt(j,0).toString()+"\n");
                    salvar.write(modelV.getValueAt(j,1).toString()+"\n");
                    salvar.write(modelV.getValueAt(j,2).toString()+"\n");
                    salvar.write(modelV.getValueAt(j,3).toString()+"\n");
                    salvar.write(modelV.getValueAt(j,4).toString()+"\n");
                }
                salvar.close();
                JOptionPane.showMessageDialog(null,"El vehículo se agregó correctamente!");
                //}
        }
        catch(Exception x){
            JOptionPane.showMessageDialog(null,"Error");

        }
        

    }//GEN-LAST:event_BotonAgregarVActionPerformed


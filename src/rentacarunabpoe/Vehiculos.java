    private void botonUsarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonUsarVehiculoActionPerformed
        // TODO add your handling code here:


        int Filaseleccionada=TablaVehiculos.getSelectedRow();

        if(Filaseleccionada>=0){
            
            String Datos[]=new String[5];
            Datos[0]=TablaVehiculos.getValueAt(Filaseleccionada,0).toString();
            Datos[1]=TablaVehiculos.getValueAt(Filaseleccionada,1).toString();
            Datos[2]=TablaVehiculos.getValueAt(Filaseleccionada,2).toString();
            Datos[3]=TablaVehiculos.getValueAt(Filaseleccionada,3).toString();
            
            if(Datos[3].equals("Disponible")){
                Arriendo a=new Arriendo();
                a.setVisible(true);
                Arriendo.modelV2.addRow(Datos);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"El auto se encuentra arrendado");
            }
        }
    }//GEN-LAST:event_botonUsarVehiculoActionPerformed

